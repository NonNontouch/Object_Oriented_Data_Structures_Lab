class Queue:
    def __init__(self, items=None):
        if items == None:
            self.items = []
        else:
            self.items = items
        self.bomb = []
        self.count = 0
        self.countbombfail = 0

    def enQueue(self, i):
        self.items.append(i)

    def dequeue(self, index=None):
        if self.size() == 0:
            return None
        elif index == None:
            return self.items.pop(0)
        else:
            return self.items.pop(index)

    def isempty(self):
        return len(self.items) == 0

    def size(self):
        return len(self.items)

    def getbomb(self):
        if len(self.bomb) != 0:
            return self.bomb.pop(0)

    def findtripletext(self):
        bombplace = False
        for i in range(2, len(self.items), 1):
            if self.items[i-2] == self.items[i-1] == self.items[i]:
                if len(self.bomb) != 0:
                    self.items.insert(i, self.bomb.pop(0))
                    bombplace = True
                if self.items[i-2] == self.items[i-1] == self.items[i]:
                    for j in range(i, i-3, -1):
                        self.dequeue(j)
                    if bombplace:
                        bombplace = False
                        self.countbombfail += 1
                        self.count-=1
                    self.count += 1
                    return


